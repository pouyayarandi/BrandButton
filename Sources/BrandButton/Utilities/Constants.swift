import Foundation

struct Constants {

    /// Duration of the animation for transition between states
    static let animationDelay: TimeInterval = 0

    /// Delay of the animation for transition between states
    static let animationDuration: TimeInterval = 0.15

    /// Horizontal space between contents of the button (label and image)
    static let contentSpacing: CGFloat = 12

    /// Corner radius of the button
    static let cornerRadius: CGFloat = 4

    /// Padding of button content in horizontal axis
    static let horizontalPadding: CGFloat = 16

    /// Padding of button content in vertical axis
    static let verticalPadding: CGFloat = 10

    /// Height of button content in regular size
    static let regularContentHeight: CGFloat = 24

    /// Height of button content in medium size
    static let mediumContentHeight: CGFloat = 32

    /// Width of button border in regular size
    static let regularBorderWidth: CGFloat = 1

    /// Width of button border in medium size
    static let mediumBorderWidth: CGFloat = 1.5

    /// Font size of the button label in regular size
    static let regularFontSize: CGFloat = 15

    /// Font size of the button label in medium size
    static let mediumFontSize: CGFloat = 17

}
